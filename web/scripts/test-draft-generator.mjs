// Runtime test for the draft generator dispatcher.
//
// Covers the three states `/api/drafts` POST can land in once the generator
// seam is wired:
//
//   1. KEEPSAKE_DRAFT_SOURCE unset / "mock" → mock generator, valid draft.
//   2. KEEPSAKE_DRAFT_SOURCE=openai + missing KEEPSAKE_DRAFT_API_KEY → 500
//      with a misconfigured error (we never fall back to mock silently).
//   3. KEEPSAKE_DRAFT_SOURCE=openai pointed at a LOCAL stub that speaks the
//      chat-completions JSON contract → 200 with a valid MessageDraft whose
//      tone, paragraphs, and assistantNote came from the stub. The stub
//      records the request so we can assert the LLM was actually called.
//
// No real network is ever touched. Run via: pnpm test:draft-generator

import { spawn } from "node:child_process";
import { createServer } from "node:http";
import { dirname, resolve } from "node:path";
import { setTimeout as wait } from "node:timers/promises";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const projectRoot = resolve(__dirname, "..");
const nextBin = resolve(projectRoot, "node_modules/.bin/next");
const BASE_PORT = Number(process.env.TEST_DRAFT_GENERATOR_PORT ?? 3160);
const STUB_PORT = Number(process.env.TEST_DRAFT_GENERATOR_STUB_PORT ?? 3170);

const stubCalls = [];
let stubNextResponse = null;

function resetStub() {
  stubCalls.length = 0;
  stubNextResponse = null;
}

function chatCompletionPayload(contentObj) {
  return {
    id: "stub-cmpl",
    object: "chat.completion",
    model: "stub-model",
    choices: [
      {
        index: 0,
        message: { role: "assistant", content: JSON.stringify(contentObj) },
        finish_reason: "stop",
      },
    ],
  };
}

function startStub() {
  return new Promise((resolveStarted, reject) => {
    const server = createServer((req, res) => {
      if (req.method !== "POST" || !req.url?.endsWith("/chat/completions")) {
        res.statusCode = 404;
        res.end();
        return;
      }
      let body = "";
      req.on("data", (chunk) => { body += chunk; });
      req.on("end", () => {
        let parsed = null;
        try { parsed = JSON.parse(body); } catch {}
        stubCalls.push({
          authorization: req.headers.authorization ?? null,
          body: parsed,
        });
        const payload = stubNextResponse ?? chatCompletionPayload({
          tone: "tender-intimate",
          toneLabel: "Tender & intimate",
          subject: "Just thinking of you",
          paragraphs: [
            { text: "Lin," },
            { text: "Stub-routed prose — proves the LLM seam actually executed." },
            { text: "— always" },
          ],
          assistantNote: "Generated by the stub LLM.",
        });
        res.statusCode = 200;
        res.setHeader("content-type", "application/json");
        res.end(JSON.stringify(payload));
      });
    });
    server.on("error", reject);
    server.listen(STUB_PORT, "127.0.0.1", () => resolveStarted(server));
  });
}

async function waitForReady(port) {
  const deadline = Date.now() + 60_000;
  while (Date.now() < deadline) {
    try {
      const res = await fetch(`http://localhost:${port}/api/people`);
      if (res.ok) return;
    } catch {}
    await wait(500);
  }
  throw new Error(`dev server did not become ready on :${port}`);
}

async function stopServer(child) {
  if (child.exitCode !== null || child.signalCode !== null) return;
  child.kill("SIGTERM");
  const exited = await Promise.race([
    new Promise((r) => child.once("exit", () => r(true))),
    wait(3_000).then(() => false),
  ]);
  if (!exited) child.kill("SIGKILL");
}

const failures = [];
function check(name, cond, detail = "") {
  if (cond) process.stdout.write(`  ✓ ${name}\n`);
  else {
    process.stdout.write(`  ✗ ${name}${detail ? `  (${detail})` : ""}\n`);
    failures.push(name);
  }
}

function childEnv(extra) {
  const env = {
    ...process.env,
    BROWSER: "none",
    NEXT_TELEMETRY_DISABLED: "1",
    KEEPSAKE_DATA_SOURCE: "mock",
  };
  // Wipe any KEEPSAKE_DRAFT_* from the harness shell so each phase starts
  // with the env we explicitly set, not whatever the host inherited.
  for (const key of Object.keys(env)) {
    if (key.startsWith("KEEPSAKE_DRAFT_")) delete env[key];
  }
  return { ...env, ...extra };
}

async function runPhase({ name, port, env, body, run }) {
  const baseUrl = `http://localhost:${port}`;
  const child = spawn(nextBin, ["dev", "--port", String(port)], {
    cwd: projectRoot,
    stdio: ["ignore", "pipe", "pipe"],
    env: childEnv(env),
  });
  let serverError = "";
  child.stderr.on("data", (chunk) => { serverError += chunk.toString(); });

  try {
    process.stdout.write(`booting next dev on :${port} for ${name}...\n`);
    await waitForReady(port);
    process.stdout.write("server ready:\n");

    const res = await fetch(`${baseUrl}/api/drafts`, {
      method: "POST",
      headers: { "content-type": "application/json" },
      body: JSON.stringify(body),
    });
    const respBody = res.headers.get("content-type")?.includes("json")
      ? await res.json().catch(() => null)
      : null;

    await run({ status: res.status, body: respBody });
  } catch (error) {
    process.stdout.write(`harness error for ${name}: ${error?.message ?? error}\n`);
    if (serverError) process.stdout.write(serverError);
    failures.push(name);
  } finally {
    await stopServer(child);
  }
}

const stub = await startStub();
process.stdout.write(`stub provider listening on :${STUB_PORT}\n`);

const linRequest = {
  personId: "p-lin",
  occasionId: "occ-lin-anniv",
  userInstruction: "",
};

try {
  await runPhase({
    name: "default mock generator (KEEPSAKE_DRAFT_SOURCE unset)",
    port: BASE_PORT,
    env: {},
    body: linRequest,
    run: ({ status, body }) => {
      check("mock default -> 200", status === 200, `status=${status}`);
      check("mock default returns valid tone",
        body && typeof body.tone === "string" && body.tone === "tender-intimate",
        `tone=${body?.tone}`);
      check("mock default paragraphs are non-empty",
        Array.isArray(body?.paragraphs) && body.paragraphs.length > 0);
      check("stub was NOT called during mock run",
        stubCalls.length === 0, `stubCalls=${stubCalls.length}`);
    },
  });

  await runPhase({
    name: "openai source + missing API key",
    port: BASE_PORT + 1,
    env: { KEEPSAKE_DRAFT_SOURCE: "openai" },
    body: linRequest,
    run: ({ status, body }) => {
      check("misconfigured -> 500", status === 500, `status=${status}`);
      check("misconfigured error mentions the generator",
        typeof body?.error === "string" && /misconfigured|unavailable/i.test(body.error),
        `error=${body?.error}`);
      check("stub still NOT called when misconfigured",
        stubCalls.length === 0, `stubCalls=${stubCalls.length}`);
    },
  });

  resetStub();
  await runPhase({
    name: "openai source + local stub",
    port: BASE_PORT + 2,
    env: {
      KEEPSAKE_DRAFT_SOURCE: "openai",
      KEEPSAKE_DRAFT_API_BASE: `http://127.0.0.1:${STUB_PORT}/v1`,
      KEEPSAKE_DRAFT_API_KEY: "stub-test-key",
      KEEPSAKE_DRAFT_MODEL: "stub-model",
    },
    body: linRequest,
    run: ({ status, body }) => {
      check("stub-backed -> 200", status === 200, `status=${status}`);
      check("stub-backed echoes stub tone",
        body?.tone === "tender-intimate", `tone=${body?.tone}`);
      check("stub-backed echoes stub subject",
        body?.subject === "Just thinking of you", `subject=${body?.subject}`);
      check("stub-backed paragraphs came from stub",
        Array.isArray(body?.paragraphs) && body.paragraphs.some(
          (p) => p.text?.includes("Stub-routed prose"),
        ));
      check("stub-backed has card from deterministic mapping",
        body?.attachedCard !== null && typeof body?.attachedCard?.styleLabel === "string");
      check("stub-backed has quick actions from deterministic mapping",
        Array.isArray(body?.quickActions) && body.quickActions.length > 0);
      check("stub-backed assistantNote from stub",
        body?.assistantNote === "Generated by the stub LLM.",
        `assistantNote=${body?.assistantNote}`);
      check("stub was called exactly once",
        stubCalls.length === 1, `stubCalls=${stubCalls.length}`);
      check("stub got the bearer token",
        stubCalls[0]?.authorization === "Bearer stub-test-key",
        `authorization=${stubCalls[0]?.authorization}`);
      check("stub got the configured model",
        stubCalls[0]?.body?.model === "stub-model",
        `model=${stubCalls[0]?.body?.model}`);
      check("stub got system + user messages",
        Array.isArray(stubCalls[0]?.body?.messages) && stubCalls[0].body.messages.length === 2);
    },
  });

  // 4th phase: malformed provider response → 500
  resetStub();
  stubNextResponse = {
    id: "stub-cmpl",
    object: "chat.completion",
    model: "stub-model",
    choices: [
      {
        index: 0,
        message: { role: "assistant", content: "this is not JSON" },
        finish_reason: "stop",
      },
    ],
  };
  await runPhase({
    name: "openai source + stub returns garbage",
    port: BASE_PORT + 3,
    env: {
      KEEPSAKE_DRAFT_SOURCE: "openai",
      KEEPSAKE_DRAFT_API_BASE: `http://127.0.0.1:${STUB_PORT}/v1`,
      KEEPSAKE_DRAFT_API_KEY: "stub-test-key",
      KEEPSAKE_DRAFT_MODEL: "stub-model",
    },
    body: linRequest,
    run: ({ status, body }) => {
      check("malformed -> 500", status === 500, `status=${status}`);
      check("malformed error stays generic",
        typeof body?.error === "string" && !/json|provider|stub-test-key/i.test(body.error),
        `error=${body?.error}`);
    },
  });
} finally {
  stub.close();
}

if (failures.length) {
  process.stdout.write(`\n${failures.length} failure(s)\n`);
  process.exit(1);
} else {
  process.stdout.write("\nall draft-generator runtime checks passed\n");
  process.exit(0);
}
